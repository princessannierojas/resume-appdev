document.addEventListener("DOMContentLoaded", function() {
    var jsProgressBar = new ProgressBar.Line('#javascript', {
        strokeWidth: 4, 
        easing: 'easeInOut', 
        duration: 1000,  
        color: 'black', 
        trailColor: 'gray', 
        trailWidth: 4, 
        svgStyle: {width: '100%', height: '100%'}, 
        text: {
            style: {
                position: 'absolute',
                right: '0',
                top: '3',
                padding: '5px 10px',
                fontSize: '12px',
                color: 'white',
                fontWeight: 'bold',
            }
        },
        step: function(state, bar) {
            bar.setText(Math.round(bar.value() * 100) + ' %'); 
        }
    });

    jsProgressBar.animate(0.9);
});

